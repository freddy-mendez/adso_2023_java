    private void mostrarAnimales() {
        String [] data = new String[this.menu.getListaAnimales().size()];
        for (int i=0; i<this.menu.getListaAnimales().size(); i++) {
            Animal a = this.menu.getListaAnimales().get(i);
            data[i]=a.getCodigo()+"-"+a.getNombre();
        }
        txtAnimales.setModel(new DefaultComboBoxModel<String>(data));
    }
    
    private void mostrarUsuarios() {
        String [] data = new String[this.menu.getListaUsuarios().size()];
        for (int i=0; i<this.menu.getListaUsuarios().size(); i++) {
            Usuario u = this.menu.getListaUsuarios().get(i);
            data[i]=u.getNumeroIdentidad()+"-"+u.getNombres()+" "+u.getApellidos();
        }
        txtUsuarios.setModel(new DefaultComboBoxModel<String>(data));
    }
    
    private void mostrarConsecutivo() {
        lbConsecutivo.setText(""+(this.menu.getListaHistoriales().size()+1));
    }
    
    private void mostrarFecha(Calendar hoy) {
        int dia = hoy.get(Calendar.DAY_OF_MONTH);
        int mes = hoy.get(Calendar.MONTH);
        int anho = hoy.get(Calendar.YEAR);
        
        lbFecha.setText(dia+"/"+mes+"/"+anho);
    }

