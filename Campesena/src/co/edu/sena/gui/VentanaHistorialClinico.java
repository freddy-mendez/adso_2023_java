    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        if (txtProcedimeintos.getSelectedIndex()==0) {
            JOptionPane.showMessageDialog(null,"Debe seleccionar un procedimiento");
            txtProcedimeintos.requestFocus();
        } else {
            String fecha = lbFecha.getText();
            int consecutivo = Integer.parseInt(lbConsecutivo.getText());
            String codigoAnimal = ((String)txtAnimales.getSelectedItem()).split("-")[0];
            String procedimiento = (String) txtProcedimeintos.getSelectedItem();
            long idUsuario = Long.parseLong(((String)txtUsuarios.getSelectedItem()).split("-")[0]);
            String observaciones = txtObservaciones.getText();
            HistorialClinico clinico = new HistorialClinico(consecutivo, new Date(fecha), codigoAnimal, procedimiento, idUsuario, observaciones);
            this.menu.getListaHistoriales().add(clinico);
            limpiarCajas();
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed

    private void limpiarCajas() {
        txtObservaciones.setText(null);
        txtProcedimeintos.setSelectedIndex(0);
        txtAnimales.setSelectedIndex(0);
    }
    
    
