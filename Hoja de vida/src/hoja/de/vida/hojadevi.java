    private void boxEducacionItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_boxEducacionItemStateChanged
        // TODO add your handling code here:
        String estudio = (String) boxEducacion.getSelectedItem();
        System.out.println("----"+estudio+"-----");
        if (estudio.equals("Tecnologo")) {
            trimestres.setMaximum(9);
            trimestres.setMinimum(1);
            DefaultListModel<String> datos_lista = new DefaultListModel<>();
            datos_lista.addElement("ADSO");
            jList1.setModel(datos_lista);
        } else if (estudio.equals("Tecnico")) {
            trimestres.setMaximum(3);
            trimestres.setMinimum(1);
            DefaultListModel<String> datos_lista = new DefaultListModel<>();
            datos_lista.addAll(niveles_formacion.get("Tecnico"));
            jList1.setModel(datos_lista);
        } else if (estudio.equals("Operario")) {
            trimestres.setMaximum(2);
            trimestres.setMinimum(1);
            DefaultListModel<String> datos_lista = new DefaultListModel<>();
            datos_lista.addAll(niveles_formacion.get("Operario"));
            jList1.setModel(datos_lista);
        }
        
    }//GEN-LAST:event_boxEducacionItemStateChanged

